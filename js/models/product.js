class Product {
    constructor(id, name, price, imgURL, category, createDate, description, detail, productID, promotion, size, updateDate) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.imgURL = imgURL;
        this.category = category;
        this.createDate = createDate;
        this.description = description;
        this.detail = detail;
        this.productID = productID;
        this.promotion = promotion;
        this.size = size;
        this.updateDate = updateDate;
    }
}

export default Product;
